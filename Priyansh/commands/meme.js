// ===================================================
// GET USER GENDER
// ===================================================
async function getUserGender(api, senderID) {
  try {
    const userInfo = await api.getUserInfo(senderID);
    if (userInfo && userInfo[senderID]) {
      const user = userInfo[senderID];
      if (user.gender === 'MALE' || user.gender === true) return 'male';
      else if (user.gender === 'FEMALE' || user.gender === false) return 'female';
    }
  } catch (e) {}
  return 'unknown';
}

// ===================================================
// GET GENDER NAME
// ===================================================
function getGenderName(gender) {
  const names = GENDER[gender]?.names || GENDER.unknown.names;
  return names[Math.floor(Math.random() * names.length)];
}

// ===================================================
// GET GENDER REPLY
// ===================================================
function getGenderReply(gender) {
  const replies = GENDER[gender]?.replies || GENDER.unknown.replies;
  return replies[Math.floor(Math.random() * replies.length)];
}

// ===================================================
// DETECT MOOD FROM TEXT
// ===================================================
function detectMood(text) {
  const lowerText = text.toLowerCase();
  
  // Check for emojis first
  for (const [emoji, data] of Object.entries(EMOJI_DB)) {
    if (text.includes(emoji)) {
      return { mood: data.mood, emoji: emoji, name: data.name };
    }
  }
  
  // Check for keywords
  const moodKeywords = {
    happy: ['😂', '🤣', 'haha', 'hehe', 'lol', 'funny', 'joke', 'हंसी', 'मजा', 'खुश'],
    sad: ['😢', '😭', 'sad', 'उदास', 'दुखी', 'pain', 'cry', 'रोना', 'दर्द'],
    angry: ['😡', '🤬', 'angry', 'गुस्सा', 'fuck', 'manyak', 'कमीना'],
    love: ['❤️', '💕', 'love', 'लव', 'प्यार', 'मोहब्बत', 'baby', 'crush'],
    attitude: ['😎', '🔥', 'attitude', 'अटीट्यूड', 'king', 'बादशाह'],
    india: ['🇮🇳', 'india', 'इंडिया', 'भारत', 'हिंदुस्तान'],
    friendship: ['🤝', 'friend', 'दोस्त', 'दोस्ती', 'bro', 'bhai', 'यार'],
    life: ['🌅', 'life', 'लाइफ', 'जिंदगी', 'जीवन'],
    food: ['🍕', '🍔', 'खाना', 'भूख', 'पिज्जा', 'बर्गर'],
    sleep: ['😴', 'sleep', 'नींद', 'सोना', 'thak']
  };
  
  for (const [mood, keywords] of Object.entries(moodKeywords)) {
    for (const keyword of keywords) {
      if (lowerText.includes(keyword.toLowerCase())) {
        return { mood: mood, emoji: null, name: mood };
      }
    }
  }
  
  return { mood: 'general', emoji: null, name: 'general' };
}

// ===================================================
// DECIDE JOKE OR SHAYARI
// ===================================================
function decideJokeOrShayari(mood) {
  const isJoke = Math.random() < 0.7; // 70% joke, 30% shayari
  
  if (isJoke) {
    if (JOKES[mood] && JOKES[mood].length > 0) {
      const jokesArray = JOKES[mood];
      return {
        type: 'joke',
        text: jokesArray[Math.floor(Math.random() * jokesArray.length)]
      };
    } else {
      const generalJokes = JOKES.general;
      return {
        type: 'joke',
        text: generalJokes[Math.floor(Math.random() * generalJokes.length)]
      };
    }
  } else {
    if (SHAYARI[mood] && SHAYARI[mood].length > 0) {
      const shayariArray = SHAYARI[mood];
      return {
        type: 'shayari',
        text: shayariArray[Math.floor(Math.random() * shayariArray.length)]
      };
    } else {
      const loveShayari = SHAYARI.love;
      return {
        type: 'shayari',
        text: loveShayari[Math.floor(Math.random() * loveShayari.length)]
      };
    }
  }
}

